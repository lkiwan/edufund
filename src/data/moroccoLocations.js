// Helper function to remove accents from text
const removeAccents = (str) => {
  return str
    .normalize('NFD') // Decompose accented characters
    .replace(/[\u0300-\u036f]/g, '') // Remove diacritical marks
    .toLowerCase();
};

// Helper function to search locations
export const searchLocations = (query) => {
  if (!query || query.length < 2) return [];

  const normalizedQuery = removeAccents(query);
  return moroccoLocations
    .filter(location => {
      const normalizedName = removeAccents(location.name);
      const normalizedRegion = removeAccents(location.region);
      return normalizedName.includes(normalizedQuery) || normalizedRegion.includes(normalizedQuery);
    })
    .slice(0, 10); // Limit to 10 results
};

// Helper function to get location display text
export const getLocationDisplay = (location) => {
  const typeLabel = location.type === 'city' ? 'Ville' : 'Rural';
  return `${location.name} (${typeLabel} - ${location.region})`;
};
